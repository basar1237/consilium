export const SectionNavBlockComponent: React.FC<SectionNavBlock> = ({ sections }) => {
  const [active, setActive] = useState<string>('')
  const items = sections || []

  useEffect(() => {
    const observer = new IntersectionObserver(
      (entries) => {
        const visible = entries.filter((e) => e.isIntersecting)
        if (visible.length > 0) {
          setActive(visible[0].target.id)
        }
      },
      { rootMargin: '-40% 0px -55% 0px' },
    )

    items.forEach(({ anchorId }) => {
      const el = document.getElementById(anchorId)
      if (el) observer.observe(el)
    })

    return () => observer.disconnect()
  }, [items])

  const scrollTo = (id: string) => {
    const el = document.getElementById(id)
    if (el) {
      el.scrollIntoView({ behavior: 'smooth', block: 'start' })
    }
  }

  return (
    <nav className="sticky top-[81px] z-40 border-b border-zinc-200 bg-white/95 backdrop-blur-sm md:top-[89px]">
      <div className="mx-auto">
        <div className="no-scrollbar flex items-center gap-1 overflow-x-auto px-4 py-2 sm:gap-2 sm:px-6 md:justify-center md:py-3 lg:px-8">
          {items.map(({ anchorId, label, icon }) => (
            <button
              key={anchorId}
              onClick={() => scrollTo(anchorId)}
              className={`flex shrink-0 cursor-pointer items-center gap-1.5 whitespace-nowrap rounded-full px-3 py-1.5 text-xs font-medium transition-all duration-200 sm:px-4 sm:py-2 sm:text-sm ${
                active === anchorId
                  ? 'bg-[#2B7DE9] text-white shadow-sm'
                  : 'text-zinc-600 hover:bg-zinc-100 hover:text-[#2B7DE9]'
              }`}
            >
              {icon && navIcons[icon]}
              <span>{label}</span>
            </button>
          ))}
        </div>
      </div>
    </nav>
  )
}
